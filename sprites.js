// Helper to create a solid colored tile
function makeSolidTile(color) {
    return Array(16).fill(null).map(() => Array(16).fill(color));
}

// Helper to create a two-tone pattern tile
function makePatternTile(c1, c2, pattern) {
    const tile = [];
    for (let y = 0; y < 16; y++) {
        const row = [];
        for (let x = 0; x < 16; x++) {
            row.push(pattern(x, y) ? c1 : c2);
        }
        tile.push(row);
    }
    return tile;
}

