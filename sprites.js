// ============================================================
// SPRITES.JS — Pixel Art Sprite System for Animal Hotel V2
// All sprites drawn via Canvas, no external images needed
// ============================================================

const PixelSprite = {
    // Cache for pre-rendered sprite canvases
    _cache: {},

    // Draw a sprite from pixel data array
    // spriteData: 2D array of hex color strings (null/0 = transparent)
    draw(ctx, spriteData, x, y, scale = 1) {
        const key = this._getCacheKey(spriteData, scale);
        if (!this._cache[key]) {
            this._cache[key] = this._renderToCanvas(spriteData, scale);
        }
        ctx.drawImage(this._cache[key], Math.floor(x), Math.floor(y));
    },

    drawFlipped(ctx, spriteData, x, y, scale = 1) {
        const key = this._getCacheKey(spriteData, scale) + '_flip';
        if (!this._cache[key]) {
            const src = this._renderToCanvas(spriteData, scale);
            const c = document.createElement('canvas');
            c.width = src.width; c.height = src.height;
            const cx = c.getContext('2d');
            cx.translate(c.width, 0);
            cx.scale(-1, 1);
            cx.drawImage(src, 0, 0);
            this._cache[key] = c;
        }
        ctx.drawImage(this._cache[key], Math.floor(x), Math.floor(y));
    },

    _getCacheKey(spriteData, scale) {
        // Use first row + dimensions as a quick key
        if (spriteData._id) return spriteData._id + '_' + scale;
        spriteData._id = 'sp_' + (++this._idCounter);
        return spriteData._id + '_' + scale;
    },
    _idCounter: 0,

    _renderToCanvas(spriteData, scale) {
        const h = spriteData.length;
        const w = spriteData[0].length;
        const canvas = document.createElement('canvas');
        canvas.width = w * scale;
        canvas.height = h * scale;
        const ctx = canvas.getContext('2d');
        for (let row = 0; row < h; row++) {
            for (let col = 0; col < w; col++) {
                const color = spriteData[row][col];
                if (color && color !== 0 && color !== '0') {
                    ctx.fillStyle = color;
                    ctx.fillRect(col * scale, row * scale, scale, scale);
                }
            }
        }
        return canvas;
    },

    clearCache() {
        this._cache = {};
    }
};

