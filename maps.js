const MapManager = {
    getMap(mapId) {
        return MAPS[mapId] || MAPS.hotel_lobby;
    },

    getTile(mapId, x, y) {
        const map = this.getMap(mapId);
        if (x < 0 || x >= map.width || y < 0 || y >= map.height) return 7; // wall
        return map.tiles[y][x];
    },

    isWalkable(mapId, x, y) {
        const map = this.getMap(mapId);
        if (x < 0 || x >= map.width || y < 0 || y >= map.height) return false;
        return map.collisions[y][x] === 0;
    },

    getExit(mapId, x, y) {
        const map = this.getMap(mapId);
        return map.exits.find(e => e.x === x && e.y === y) || null;
    },

    getEntitiesAt(mapId, x, y) {
        const map = this.getMap(mapId);
        return map.entities.filter(e => e.x === x && e.y === y);
    }
};
